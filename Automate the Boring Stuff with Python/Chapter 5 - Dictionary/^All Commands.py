# keys(), values() and items() for dictionary 
dic = {'key1':'value1','key2':'value2','key3':'value3'}
print(dic.keys())
print(dic.values())
print(dic.items())

# get() for dictionary 
dic = {'key1':'value1','key2':'value2','key3':'value3'}
print(dic.get('key2'))