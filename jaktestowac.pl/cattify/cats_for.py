# Cats inventory advanced manager v2 (with for)
print("Starting program")

print("Preparing data")
cat1 = "Kitty"
cat2 = "Pussy"
cat3 = "Paw"

print("Initialising cats list")
cats = [cat1, cat2, cat3]

print("Returning cats")
for cat in cats:
    print(f"Found cat: {cat}")

print("Cat program finished")